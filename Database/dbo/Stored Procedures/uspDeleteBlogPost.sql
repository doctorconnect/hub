
CREATE PROCEDURE [dbo].[uspDeleteBlogPost]
(
@Blogld INT 
)
AS
BEGIN 
BEGIN TRANSACTION; 
SAVE TRANSACTION MySavePoint; 
BEGIN TRY 
BEGIN 
DELETE FROM Blogs WHERE Id = @Blogld; 
END 
END TRY 
BEGIN CATCH 
 IF @@TRANCOUNT>0
BEGIN 
ROLLBACK TRANSACTION MySavePoint; 
END 
END CATCH 
COMMIT TRANSACTION 
END 
